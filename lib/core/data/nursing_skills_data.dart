  /// Get all skills including user's custom skills (alphabetically sorted)
  static List<String> getAllSkills(List<String> customSkills) {
    final allSkills = [...defaultSkills, ...customSkills];
    allSkills.sort((a, b) => a.toLowerCase().compareTo(b.toLowerCase()));
    return allSkills;
  }

  /// Filter skills by search query
  static List<String> filterSkills(List<String> skills, String query) {
    if (query.isEmpty) return skills;
    final lowerQuery = query.toLowerCase();
    return skills
        .where((skill) => skill.toLowerCase().contains(lowerQuery))
        .toList();
  }
