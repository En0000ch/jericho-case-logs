  /// Get the SharedPreferences key for a specialty
  static String getKeyForSpecialty(String specialty) {
    return specialtyToKey[specialty] ?? 'jclSurgeryArray_$specialty';
  }

  /// Get default surgeries for a specialty (alphabetized)
  static List<String> getDefaultSurgeriesForSpecialty(String specialty) {
    final surgeries = List<String>.from(defaultSurgeries[specialty] ?? []);
    surgeries.sort((a, b) => a.toLowerCase().compareTo(b.toLowerCase()));
    return surgeries;
  }
